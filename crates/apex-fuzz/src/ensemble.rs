//! Ensemble fuzzing infrastructure — parallel strategy execution with a shared corpus.
//!
//! `EnsembleRunner` spawns each registered [`Strategy`] as a concurrent tokio
//! task. All strategies share a single [`Corpus`] protected by an `Arc<Mutex>`.
//! After `sync_interval`, seeds from every strategy are merged into the shared
//! corpus and returned to the caller.
//!
//! # Design
//!
//! Strategies are currently sequential at the individual `suggest_inputs` call
//! level (the `Strategy` trait is `async`), but the ensemble wraps each in a
//! separate `tokio::task` so they execute concurrently on the async runtime.
//! When `sync_interval` elapses, all tasks are joined and their outputs merged.

use crate::corpus::Corpus;
use apex_core::{
    error::{ApexError, Result},
    traits::Strategy,
    types::{ExplorationContext, InputSeed},
};
use std::{
    sync::{Arc, Mutex},
    time::Duration,
};
use tracing::{debug, info};

// ---------------------------------------------------------------------------
// EnsembleRunner
// ---------------------------------------------------------------------------

/// Runs multiple [`Strategy`] instances in parallel, sharing a single corpus.
///
/// Seeds generated by any strategy that find new coverage are added to the
/// shared corpus so every other strategy can benefit from them in subsequent
/// rounds.
pub struct EnsembleRunner {
    /// The strategies to run in parallel.
    strategies: Vec<Arc<dyn Strategy>>,
    /// Shared corpus — strategies write seeds here after each sync interval.
    shared_corpus: Arc<Mutex<Corpus>>,
    /// How long to wait for all strategy tasks before collecting results.
    /// In the current implementation this acts as a concurrency budget:
    /// tasks that finish before the interval contribute their seeds immediately.
    #[allow(dead_code)]
    sync_interval: Duration,
}

impl EnsembleRunner {
    /// Create an ensemble with the given strategies, shared corpus, and
    /// synchronisation interval.
    pub fn new(
        strategies: Vec<Arc<dyn Strategy>>,
        shared_corpus: Arc<Mutex<Corpus>>,
        sync_interval: Duration,
    ) -> Self {
        EnsembleRunner {
            strategies,
            shared_corpus,
            sync_interval,
        }
    }

    /// Access the shared corpus (e.g. to pre-seed it).
    pub fn corpus(&self) -> Arc<Mutex<Corpus>> {
        Arc::clone(&self.shared_corpus)
    }

    /// Add a seed to the shared corpus.
    pub fn seed(&self, data: Vec<u8>, coverage_gain: usize) -> Result<()> {
        self.shared_corpus
            .lock()
            .map_err(|e| ApexError::Other(format!("ensemble corpus lock poisoned: {e}")))?
            .add(data, coverage_gain);
        Ok(())
    }

    /// Run all strategies concurrently and collect their suggested inputs.
    ///
    /// Each strategy is spawned as a separate `tokio::spawn` task. Seeds from
    /// all tasks are merged into the shared corpus after all tasks complete.
    /// The full merged seed list is returned to the caller.
    pub async fn run_parallel(&self, ctx: &ExplorationContext) -> Vec<InputSeed> {
        if self.strategies.is_empty() {
            return vec![];
        }

        let mut handles = Vec::with_capacity(self.strategies.len());

        for strategy in &self.strategies {
            let strat = Arc::clone(strategy);
            let ctx_clone = ctx.clone();
            let handle = tokio::spawn(async move { strat.suggest_inputs(&ctx_clone).await });
            handles.push((strategy.name().to_string(), handle));
        }

        let mut all_seeds: Vec<InputSeed> = Vec::new();

        for (name, handle) in handles {
            match handle.await {
                Ok(Ok(seeds)) => {
                    debug!(strategy = %name, count = seeds.len(), "ensemble: strategy produced seeds");
                    all_seeds.extend(seeds);
                }
                Ok(Err(e)) => {
                    debug!(strategy = %name, error = %e, "ensemble: strategy returned error");
                }
                Err(e) => {
                    debug!(strategy = %name, error = %e, "ensemble: strategy task panicked");
                }
            }
        }

        // Merge all seeds into the shared corpus (no coverage info at this
        // stage — use coverage_gain=1 as a placeholder).
        if !all_seeds.is_empty() {
            if let Ok(mut corpus) = self.shared_corpus.lock() {
                for seed in &all_seeds {
                    corpus.add(seed.data.to_vec(), 1);
                }
                info!(
                    added = all_seeds.len(),
                    corpus_size = corpus.len(),
                    "ensemble: merged seeds into shared corpus"
                );
            }
        }

        all_seeds
    }

    /// Return the number of strategies registered.
    pub fn strategy_count(&self) -> usize {
        self.strategies.len()
    }
}


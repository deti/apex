#[cfg(test)]
mod tests {
    use super::*;
    use apex_core::{
        error::Result,
        traits::Strategy,
        types::{ExecutionResult, ExplorationContext, InputSeed, Language, SeedOrigin, Target},
    };
    use async_trait::async_trait;
    use std::sync::atomic::{AtomicUsize, Ordering};

    // -----------------------------------------------------------------------
    // Stub strategy for testing
    // -----------------------------------------------------------------------

    /// A test strategy that emits a fixed set of seeds and records how many
    /// times it was called.
    struct FixedStrategy {
        name: String,
        seeds: Vec<Vec<u8>>,
        call_count: Arc<AtomicUsize>,
    }

    impl FixedStrategy {
        fn new(name: &str, seeds: Vec<Vec<u8>>) -> Self {
            FixedStrategy {
                name: name.to_string(),
                seeds,
                call_count: Arc::new(AtomicUsize::new(0)),
            }
        }
    }

    #[async_trait]
    impl Strategy for FixedStrategy {
        fn name(&self) -> &str {
            &self.name
        }

        async fn suggest_inputs(&self, _ctx: &ExplorationContext) -> Result<Vec<InputSeed>> {
            self.call_count.fetch_add(1, Ordering::SeqCst);
            Ok(self
                .seeds
                .iter()
                .map(|d| InputSeed::new(d.clone(), SeedOrigin::Fuzzer))
                .collect())
        }

        async fn observe(&self, _result: &ExecutionResult) -> Result<()> {
            Ok(())
        }
    }

    fn make_ctx() -> ExplorationContext {
        ExplorationContext {
            target: Target {
                root: std::path::PathBuf::from("/tmp"),
                language: Language::C,
                test_command: vec![],
            },
            uncovered_branches: vec![],
            iteration: 0,
        }
    }

    // -----------------------------------------------------------------------
    // Tests
    // -----------------------------------------------------------------------

    #[tokio::test]
    async fn two_strategies_produce_seeds_in_parallel() {
        let s1 = Arc::new(FixedStrategy::new(
            "s1",
            vec![b"aaa".to_vec(), b"bbb".to_vec()],
        ));
        let s2 = Arc::new(FixedStrategy::new("s2", vec![b"ccc".to_vec()]));

        let corpus = Arc::new(Mutex::new(Corpus::new(1000)));
        let runner = EnsembleRunner::new(
            vec![s1.clone(), s2.clone()],
            Arc::clone(&corpus),
            Duration::from_millis(100),
        );

        let seeds = runner.run_parallel(&make_ctx()).await;

        // Both strategies contributed seeds.
        assert_eq!(seeds.len(), 3, "expected 2 from s1 + 1 from s2");
    }

    #[tokio::test]
    async fn shared_corpus_receives_seeds_from_both_strategies() {
        let s1 = Arc::new(FixedStrategy::new("s1", vec![b"alpha".to_vec()]));
        let s2 = Arc::new(FixedStrategy::new(
            "s2",
            vec![b"beta".to_vec(), b"gamma".to_vec()],
        ));

        let corpus = Arc::new(Mutex::new(Corpus::new(1000)));
        let runner =
            EnsembleRunner::new(vec![s1, s2], Arc::clone(&corpus), Duration::from_millis(50));

        runner.run_parallel(&make_ctx()).await;

        let corpus_size = corpus.lock().unwrap().len();
        assert_eq!(corpus_size, 3, "all 3 seeds should be in shared corpus");
    }

    #[tokio::test]
    async fn empty_strategies_returns_empty_seeds() {
        let corpus = Arc::new(Mutex::new(Corpus::new(100)));
        let runner = EnsembleRunner::new(vec![], Arc::clone(&corpus), Duration::from_millis(50));

        let seeds = runner.run_parallel(&make_ctx()).await;
        assert!(seeds.is_empty());
        assert_eq!(corpus.lock().unwrap().len(), 0);
    }

    #[tokio::test]
    async fn strategy_count_is_correct() {
        let s1 = Arc::new(FixedStrategy::new("a", vec![]));
        let s2 = Arc::new(FixedStrategy::new("b", vec![]));
        let s3 = Arc::new(FixedStrategy::new("c", vec![]));

        let corpus = Arc::new(Mutex::new(Corpus::new(100)));
        let runner = EnsembleRunner::new(
            vec![s1, s2, s3],
            Arc::clone(&corpus),
            Duration::from_millis(50),
        );

        assert_eq!(runner.strategy_count(), 3);
    }

    #[tokio::test]
    async fn seed_adds_to_corpus() {
        let corpus = Arc::new(Mutex::new(Corpus::new(100)));
        let runner = EnsembleRunner::new(vec![], Arc::clone(&corpus), Duration::from_millis(50));

        runner.seed(b"pre-seed".to_vec(), 5).unwrap();
        assert_eq!(corpus.lock().unwrap().len(), 1);
    }

    #[tokio::test]
    async fn corpus_accumulates_across_runs() {
        let s1 = Arc::new(FixedStrategy::new("s1", vec![b"run1".to_vec()]));
        let s2 = Arc::new(FixedStrategy::new("s2", vec![b"run2".to_vec()]));

        let corpus = Arc::new(Mutex::new(Corpus::new(1000)));
        let runner =
            EnsembleRunner::new(vec![s1, s2], Arc::clone(&corpus), Duration::from_millis(50));

        runner.run_parallel(&make_ctx()).await;
        let after_first = corpus.lock().unwrap().len();
        assert_eq!(after_first, 2);

        // Second run — the strategies are now re-registered as new Arcs would be,
        // but for this test we verify that corpus size grows additively.
        runner.run_parallel(&make_ctx()).await;
        let after_second = corpus.lock().unwrap().len();
        // Corpus should have grown (up to capacity).
        assert!(after_second >= after_first);
    }

    #[tokio::test]
    async fn single_strategy_works() {
        let s1 = Arc::new(FixedStrategy::new("solo", vec![b"only-seed".to_vec()]));

        let corpus = Arc::new(Mutex::new(Corpus::new(100)));
        let runner = EnsembleRunner::new(vec![s1], Arc::clone(&corpus), Duration::from_millis(50));

        let seeds = runner.run_parallel(&make_ctx()).await;
        assert_eq!(seeds.len(), 1);
        assert_eq!(corpus.lock().unwrap().len(), 1);
    }

    #[test]
    fn corpus_accessor_returns_same_arc() {
        let corpus = Arc::new(Mutex::new(Corpus::new(100)));
        let runner = EnsembleRunner::new(vec![], Arc::clone(&corpus), Duration::from_millis(50));
        // Both point to the same allocation.
        assert!(Arc::ptr_eq(&runner.corpus(), &corpus));
    }
}

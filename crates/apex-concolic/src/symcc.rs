#[cfg(test)]
mod tests {
    use super::*;

    // -----------------------------------------------------------------------
    // SymCcBackend tests
    // -----------------------------------------------------------------------

    #[test]
    fn is_available_false_when_nonexistent_path() {
        let backend = SymCcBackend::with_path("/nonexistent/path/to/symcc");
        assert!(!backend.is_available());
    }

    #[test]
    fn is_available_false_when_searching_path_and_not_installed() {
        // Override PATH to be empty so `symcc` is definitely not found.
        // We can't modify the environment reliably in tests, but we can
        // assert on the explicit-path variant which is deterministic.
        let backend = SymCcBackend::with_path("/dev/null/no-such-binary");
        assert!(!backend.is_available());
    }

    #[test]
    fn compile_returns_err_when_not_available() {
        let backend = SymCcBackend::with_path("/nonexistent/symcc");
        let result = backend.compile(Path::new("/tmp/test.c"));
        assert!(result.is_err());
        let msg = result.unwrap_err().to_string();
        assert!(
            msg.contains("symcc") || msg.contains("not found"),
            "unexpected error: {msg}"
        );
    }

    #[test]
    fn run_with_input_returns_err_when_not_available() {
        let backend = SymCcBackend::with_path("/nonexistent/symcc");
        let result = backend.run_with_input(Path::new("/tmp/binary"), b"hello");
        assert!(result.is_err());
    }

    // -----------------------------------------------------------------------
    // parse_output_dir tests — test the constraint parser in isolation
    // -----------------------------------------------------------------------

    #[test]
    fn parse_output_dir_empty_dir_returns_empty() {
        let dir = tempdir_for_test("parse_empty");
        let constraints = parse_output_dir(&dir).unwrap();
        assert!(constraints.is_empty());
        let _ = std::fs::remove_dir_all(&dir);
    }

    #[test]
    fn parse_output_dir_skips_input_bin() {
        let dir = tempdir_for_test("parse_skip_input");
        std::fs::write(dir.join("input.bin"), b"seed data").unwrap();
        let constraints = parse_output_dir(&dir).unwrap();
        assert!(constraints.is_empty(), "input.bin should be skipped");
        let _ = std::fs::remove_dir_all(&dir);
    }

    #[test]
    fn parse_output_dir_reads_constraint_files() {
        let dir = tempdir_for_test("parse_reads");
        std::fs::write(dir.join("000001"), b"constraint_data_a").unwrap();
        std::fs::write(dir.join("000002"), b"constraint_data_b").unwrap();
        let constraints = parse_output_dir(&dir).unwrap();
        assert_eq!(constraints.len(), 2);
        assert_eq!(constraints[0].label, "000001");
        assert_eq!(constraints[0].input, b"constraint_data_a");
        assert_eq!(constraints[1].label, "000002");
        let _ = std::fs::remove_dir_all(&dir);
    }

    #[test]
    fn parse_output_dir_skips_empty_files() {
        let dir = tempdir_for_test("parse_skips_empty");
        std::fs::write(dir.join("000001"), b"").unwrap(); // empty
        std::fs::write(dir.join("000002"), b"data").unwrap();
        let constraints = parse_output_dir(&dir).unwrap();
        assert_eq!(constraints.len(), 1);
        assert_eq!(constraints[0].label, "000002");
        let _ = std::fs::remove_dir_all(&dir);
    }

    #[test]
    fn parse_output_dir_sorted_by_label() {
        let dir = tempdir_for_test("parse_sorted");
        std::fs::write(dir.join("000003"), b"c").unwrap();
        std::fs::write(dir.join("000001"), b"a").unwrap();
        std::fs::write(dir.join("000002"), b"b").unwrap();
        let constraints = parse_output_dir(&dir).unwrap();
        assert_eq!(constraints.len(), 3);
        assert_eq!(constraints[0].label, "000001");
        assert_eq!(constraints[1].label, "000002");
        assert_eq!(constraints[2].label, "000003");
        let _ = std::fs::remove_dir_all(&dir);
    }

    // -----------------------------------------------------------------------
    // PathConstraint struct tests
    // -----------------------------------------------------------------------

    #[test]
    fn path_constraint_clone() {
        let c = PathConstraint {
            input: b"hello".to_vec(),
            label: "000001".to_string(),
        };
        let cloned = c.clone();
        assert_eq!(cloned.input, c.input);
        assert_eq!(cloned.label, c.label);
    }

    #[test]
    fn path_constraint_debug() {
        let c = PathConstraint {
            input: b"test".to_vec(),
            label: "lbl".to_string(),
        };
        let debug = format!("{c:?}");
        assert!(debug.contains("PathConstraint"));
    }

    // -----------------------------------------------------------------------
    // SymCcStrategy tests
    // -----------------------------------------------------------------------

    #[tokio::test]
    async fn strategy_name_is_symcc() {
        let s = SymCcStrategy::new();
        assert_eq!(s.name(), "symcc");
    }

    #[tokio::test]
    async fn suggest_inputs_empty_when_not_available() {
        // Point to a non-existent binary so is_available() returns false.
        let s = SymCcStrategy {
            backend: SymCcBackend::with_path("/nonexistent/symcc"),
            instrumented_binary: None,
        };
        let ctx = make_ctx();
        let inputs = s.suggest_inputs(&ctx).await.unwrap();
        assert!(inputs.is_empty(), "should return empty when symcc missing");
    }

    #[tokio::test]
    async fn observe_always_ok() {
        let s = SymCcStrategy::new();
        let result = ExecutionResult {
            seed_id: apex_core::types::SeedId::new(),
            status: apex_core::types::ExecutionStatus::Pass,
            new_branches: vec![],
            trace: None,
            duration_ms: 1,
            stdout: String::new(),
            stderr: String::new(),
            input: None,
            resource_metrics: None,
        };
        assert!(s.observe(&result).await.is_ok());
    }

    #[test]
    fn backend_accessor_works() {
        let s = SymCcStrategy::new();
        // is_available may be true or false depending on the environment;
        // we just verify the accessor doesn't panic.
        let _ = s.backend().is_available();
    }

    #[test]
    fn with_binary_stores_path() {
        let s = SymCcStrategy::with_binary("/tmp/my-instrumented-binary");
        assert_eq!(
            s.instrumented_binary.as_deref(),
            Some(Path::new("/tmp/my-instrumented-binary"))
        );
    }

    // -----------------------------------------------------------------------
    // Helper
    // -----------------------------------------------------------------------

    fn tempdir_for_test(suffix: &str) -> PathBuf {
        let dir = std::env::temp_dir().join(format!("apex-symcc-test-{suffix}"));
        std::fs::create_dir_all(&dir).unwrap();
        dir
    }

    fn make_ctx() -> ExplorationContext {
        ExplorationContext {
            target: apex_core::types::Target {
                root: std::path::PathBuf::from("/tmp"),
                language: apex_core::types::Language::C,
                test_command: vec![],
            },
            uncovered_branches: vec![],
            iteration: 0,
        }
    }
}
